    private boolean checkIfUserExists(String username, String email) throws Exception {
        User user1 = repository.finUserByUsername(username);
        User user2 = repository.findUserByEmail(email);
        return !(user1 != null || user2 != null);
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfFirstName, tfLastName, tfUsername, tfEmail, pfPassword);
        errorLabels = Arrays.asList(lbFirstNameError, lbLastNameError, lbUsernameError, lbEmailError, lbPasswordError);
    }

    private void initRepository() {
        repository = RepositoryFactory.getRepository();
    }
