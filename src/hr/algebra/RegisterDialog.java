    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (ValidationUtils.CheckFormValidation(validationFields, errorLabels)) {

            try {
                if (checkIfUserExists(tfUsername.getText().trim(), tfEmail.getText().trim())) {
                    String pwd = new String(pfPassword.getPassword());
                    User user = new User(
                            tfFirstName.getText().trim(),
                            tfLastName.getText().trim(),
                            tfEmail.getText().trim(),
                            tfUsername.getText().trim(),
                            pwd,
                            isAdmin);

                    try {
                        repository.createUser(user);
                    } catch (Exception ex) {
                        MessageUtils.showErrorMessage("Error", "Unable to connent to database");
                    }
                    MessageUtils.showInformationMessage("Success", "User created.");
                    this.dispose();
                } else {
                    MessageUtils.showErrorMessage("Error", "User already exists");
                }
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Error", "Unable to connent to database");
            }

        }
    }//GEN-LAST:event_btnRegisterActionPerformed

